import mongoose from 'mongoose';

const ResetPasswordSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    trim: true,
  },
  token: {
    type: String,
    required: true,
    trim: true,
  },
  expiringDate: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const ResetPassword = mongoose.model('resetPassword', ResetPasswordSchema);

export default ResetPassword;
